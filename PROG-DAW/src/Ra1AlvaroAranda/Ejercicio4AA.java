package Ra1AlvaroAranda;

public class Ejercicio4AA {
    public static void main(String[] args) {
        int num1 = 12;
        int num2 = 8;
        int suma;
        int resta;
        int multi;
        int divi;
        int resto;
        int incr;
        int decr;
        boolean mayor_que;
        boolean menor_que;
        boolean mayor_o_igual;
        boolean menor_o_igual;
        boolean igual;
        boolean distinto;
        boolean and;
        boolean or;
        boolean not;
        String ternario;






        suma = (num1 + num2); // + sirve para sumar
        resta = (num1 - num2); // - sirve para restar
        multi = (num1 * num2); // * sirve para multiplicar
        divi = (num1 / num2); // / sirve para dividir
        resto = (num1 % num2); // % sirve para calcular el resto de una division
        incr = ++num1; // ++ sirve para aumentar el valor en 1
        decr = --num1; // -- sirve para disminuir el valor en 1
        mayor_que = num1 > num2; // > sirve para definir si un valor es mayor que otro
        menor_que = num1 < num2; // < sirve para definir si un valor es menor que otro
        mayor_o_igual = num1 >= num2; // >= sirve para definir si un valor es mayor o igual que otro
        menor_o_igual = num1 <= num2; // <= sirve para definir si un valor es menor o igual que otro
        igual = num1 == num2; // == sirve para definir si un valor es igual que otro
        distinto = num1 != num2; // != sirve para definir si un valor es distinto que otro
        and = (num1 > 3) && (num2 <6); // && tienen que cumplirse ambas condiciones para que sea true
        or = num2 < 5 || num1 != 6; // || tiene que cumplirse una condicion para que eso sea true
        not = num1 != 2; // != sirve para negar una condicion, si num1 no es 2 entonces es true, si es 2 es false
        ternario = (num1 < 0) ? "negativo" : "positivo"; // se usa para evaluar una condicion y asignar un valor a una variable en funcion de su resultado






    }
}
